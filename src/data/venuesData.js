// Нормализация строки предметов в массив ключей: math, biology, physics, chemistry
function parseSubjectLine(line) {
  if (!line || typeof line !== 'string') return [];
  const normalized = line.toLowerCase().replace(/\s+/g, ' ').trim();
  const parts = normalized.split(/[\s,]+/).filter(Boolean);
  const map = {
    математика: 'math',
    биология: 'biology',
    физика: 'physics',
    химия: 'chemistry',
  };
  const set = new Set();
  for (const p of parts) {
    if (map[p]) set.add(map[p]);
  }
  return Array.from(set);
}

// Группировка по адресу (нормализованному): один адрес — объединённый список предметов
function buildVenuesBySubject() {
  const byAddress = new Map(); // normalizedAddress -> Set(subjects)
  for (const row of VENUE_SUBJECT_RAW) {
    const addr = row.address.trim().replace(/\s+/g, ' ');
    const subjects = parseSubjectLine(row.subjectLine);
    if (!byAddress.has(addr)) byAddress.set(addr, new Set());
    subjects.forEach((s) => byAddress.get(addr).add(s));
  }
  // Преобразуем в объект: subjectKey -> массив адресов
  const bySubject = { math: [], biology: [], physics: [], chemistry: [] };
  for (const [addr, subjects] of byAddress) {
    subjects.forEach((s) => {
      if (bySubject[s] && !bySubject[s].includes(addr)) bySubject[s].push(addr);
    });
  }
  return bySubject;
}

const VENUES_BY_SUBJECT = buildVenuesBySubject();

/**
 * Возвращает список адресов площадок для выбранного предмета (ключ: math, biology, physics, chemistry).
 */
export function getVenuesForSubject(subjectKey) {
  return VENUES_BY_SUBJECT[subjectKey] || [];
}

