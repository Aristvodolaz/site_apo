// Парсинг строки предметов в массив кодов: math, biology, physics, chemistry
function parseSubjectLine(line) {
  if (!line || typeof line !== 'string') return [];
  const normalized = line.toLowerCase().replace(/\s+/g, ' ').trim();
  const parts = normalized.split(/[\s,]+/).filter(Boolean);
  const map = {
    математика: 'math',
    биология: 'biology',
    физика: 'physics',
    химия: 'chemistry',
  };
  const set = new Set();
  for (const p of parts) {
    if (map[p]) set.add(map[p]);
  }
  return Array.from(set);
}

// Группировка по названию площадки: одна площадка — объединённый список предметов и один адрес
function buildVenues() {
  const byVenue = new Map(); // venueName -> { subjects: Set, address: string }

  VENUE_SUBJECT_RAW.forEach((row, index) => {
    const name = (row.venue || '').trim();
    if (!name) return;

    const subjects = parseSubjectLine(row.subjectLine);
    const address = (VENUE_ADDRESSES[index] || '').trim();

    if (!byVenue.has(name)) {
      byVenue.set(name, {
        subjects: new Set(),
        address: address || name,
      });
    }

    const entry = byVenue.get(name);
    subjects.forEach((s) => entry.subjects.add(s));
  });

  return Array.from(byVenue.entries()).map(([name, entry]) => ({
    // В БД основное отображаемое поле (name) = адрес площадки
    name: entry.address || name,
    address: entry.address || name,
    institutionName: name,
    subjects: Array.from(entry.subjects),
    rawSubjects: VENUE_SUBJECT_RAW.find((r) => (r.venue || '').trim() === name)?.subjectLine || '',
  }));
}

async function initializeVenues() {
  try {
    console.log('🚀 Начинаем загрузку площадок в Firebase...');

    const app = initializeApp(firebaseConfig);
    const db = getFirestore(app);

    const venuesRef = collection(db, 'venues');

    // Полностью очищаем коллекцию перед добавлением новых площадок,
    // чтобы остались только эти 66 актуальных записей
    console.log('🧹 Очищаем коллекцию "venues"...');
    const existing = await getDocs(venuesRef);
    for (const docSnap of existing.docs) {
      await deleteDoc(docSnap.ref);
      console.log(`  ❌ Удалена старая площадка: ${docSnap.id}`);
    }

    const venues = buildVenues();

    console.log(`📋 Всего уникальных площадок: ${venues.length}`);

    for (const venue of venues) {
      const docData = {
        name: venue.name,
        address: venue.address,
        subjects: venue.subjects,
        rawSubjects: venue.rawSubjects,
        created_at: new Date(),
      };
      await addDoc(venuesRef, docData);
      console.log(`✅ Добавлена площадка: ${venue.name} [${venue.subjects.join(', ')}]`);
    }

    console.log('🎉 Все площадки успешно загружены в коллекцию "venues".');
    process.exit(0);
  } catch (error) {
    console.error('❌ Ошибка при инициализации площадок:', error);
    process.exit(1);
  }
}

initializeVenues();

